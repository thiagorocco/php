<?php
    /*
        +=x incrementa
        -=x decrementa
        *=x multiplica por x
        /=x divide por x
    */
    $var = 100;
    $var += 5; // Soma 5 em $var;
    $var -= 5; // Subtrai 5 em $var;
    $var *=5; // Multiplica $var por 5;
    $var /= 5; // Divide $var por 5;
    print $var; // resultado: 100